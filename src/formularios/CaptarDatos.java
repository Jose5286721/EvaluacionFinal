    //Con este metodo exportamos todos los datos a un arhivo txt
    public void toFileTXT() {
        FileNameExtensionFilter filter = new FileNameExtensionFilter("TXT","txt");
        JFileChooser chos = new JFileChooser();
        chos.setSelectedFile(new File("asignatura.txt"));
        chos.setFileFilter(filter);
        chos.setAcceptAllFileFilterUsed(false);
        int  respuesta = chos.showSaveDialog(null);
        if(respuesta == chos.APPROVE_OPTION){
        try {
            File archivo = chos.getSelectedFile();
            if(!archivo.canWrite()){
        BufferedWriter bw;
        if(archivo.exists()) {
            bw = new BufferedWriter(new FileWriter(archivo));
            for (int i = 0; i < cantpers; i++) {
                bw.write(String.valueOf(nombres[i])+"\n");
                bw.write(String.valueOf(notas[i])+"\n");
            }
        } else {
            bw = new BufferedWriter(new FileWriter(archivo));
            for (int i = 0; i < cantpers; i++) {
                bw.write(String.valueOf(nombres[i])+"\n");
                bw.write(String.valueOf(notas[i])+"\n");
            }
        }
        JOptionPane.showMessageDialog(null, "Registro guardado con exito");
        bw.close();
            }else{
                JOptionPane.showMessageDialog(null, "No tiene permisos para escritura","Error",JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        }else{
            JOptionPane.showMessageDialog(null,"Se ha cancelado el guardado del archivo","Error",JOptionPane.ERROR_MESSAGE);
        }
    }
