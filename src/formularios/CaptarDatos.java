    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int cantidad = Integer.valueOf(NroEstudiantes.getText());
        cantpers = cantidad;
        Asignatura asignatura = new Asignatura();
        asignatura.setCantidadPersonas(cantidad);
        nombres = new String[cantidad];
        notas = new int[cantidad];
        for(int i=0; i<cantidad ;i++){
            String nombre = JOptionPane.showInputDialog("Introduzca el nombre del alumno");
            int nota = Integer.parseInt(JOptionPane.showInputDialog("Introduzca la nota del alumno"));
            nombres[i] = nombre;
            notas[i] = nota;
        }   
        asignatura.setNombres(nombres);
        asignatura.setNotas(notas);
        toFileTXT();
        //asignatura.leerArchivoTXT();
    }//GEN-LAST:event_jButton1ActionPerformed

